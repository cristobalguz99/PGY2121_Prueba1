    //BOTON GUARDAR CLIENTE
    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed
        
        
        Validaciones v = new Validaciones();
        
        String nombre = txtNombreCliente.getText();
        String usuario= txtUsuarioCliente.getText();
        String contrasena= txtpasswordCliente.getText();
        int rut = Integer.parseInt(txtRut.getText());
        String dv = txtDv.getText();
        String direccion = txtDireccion.getText();
        String comuna = comboComuna.getSelectedItem().toString();
        int telefono= Integer.parseInt(txtTelefono.getText());
        String correo = txtCorreo.getText();
        boolean empresa = checkEmpresa.isSelected();
        
        
        if (v.ValidarNombre(nombre, usuario, direccion)==true){
            dtoCliente regisCliente = new dtoCliente(nombre, usuario, contrasena, rut, dv, direccion, comuna, telefono, correo, empresa);
            ListarCliente ListaVentana = new ListarCliente();
            ListaVentana.GuardarCliente(regisCliente);
            txtArea.setText(ListaVentana.ListarClientes().toString());
            
        }else{
            JOptionPane.showMessageDialog(this, "Intente nuevamente", "Error en el ingreso de datos",JOptionPane.ERROR_MESSAGE);
        }
        
       
        
        
        
    }//GEN-LAST:event_btnGuardarClienteActionPerformed


